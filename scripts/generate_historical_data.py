# ── Generate docs from clusters ───────────────────────────────────────────────
def generate_all() -> list[dict]:
    docs = []
    project_num = 1

    for cluster in CLUSTERS:
        team_min, team_max       = cluster["team_size_range"]
        tenure_min, tenure_max   = cluster["tenure_range"]
        milestone_min, milestone_max = cluster["milestone_range"]

        for name, outcome, intervention, margin_impact, weeks in cluster["projects"]:
            team     = random.randint(team_min, team_max)
            tenure   = random.randint(tenure_min, tenure_max)
            milestone = random.randint(milestone_min, milestone_max)

            # Jitter the cluster centre signals
            burn      = jitter(cluster["burn"],      noise=0.10)
            velocity  = jitter(cluster["velocity"],  noise=0.10)
            sentiment = jitter(cluster["sentiment"], noise=0.08)
            margin    = round(jitter(cluster["margin"], noise=4.0), 1)

            # Worse outcomes have slightly worse signals
            severity_boost = {"lost": 0.12, "escalated": 0.06, "rescued": 0.0, "delivered_clean": -0.05}[outcome]
            burn      = round(min(burn + severity_boost, 0.95), 3)
            velocity  = round(max(velocity - severity_boost, -0.95), 3)
            sentiment = round(max(sentiment - severity_boost * 0.5, -0.95), 3)

            fp = make_fingerprint(
                burn, velocity, sentiment,
                cluster["phase"], team, tenure, milestone, margin
            )

            docs.append({
                "project_id":             f"HIST-{project_num:03d}",
                "project_name":           name,
                "industry":               cluster["industry"],
                "phase_at_detection":     cluster["phase"],
                "cluster":                cluster["id"],
                "team_size":              team,
                "client_tenure_months":   tenure,
                "days_to_milestone":      milestone,
                "burn_rate_acceleration": burn,
                "velocity_delta":         velocity,
                "sentiment_slope":        sentiment,
                "margin_at_risk_pct":     margin,
                "intervention":           intervention,
                "outcome":                outcome,
                "margin_impact_pct":      margin_impact,
                "weeks_to_resolution":    weeks,
                "partner_overrode_agent": random.random() < 0.15,
                "notes":                  f"Cluster {cluster['id']} — {cluster['industry']} {cluster['phase']} pattern",
                "closed_date":            rand_date(),
                "fingerprint":            fp,
            })
            project_num += 1

    return docs


# ── Bulk index ────────────────────────────────────────────────────────────────
def bulk_index(index: str, docs: list[dict], id_field: str):
    operations = []
    for doc in docs:
        operations.append({"index": {"_index": index, "_id": doc[id_field]}})
        operations.append(doc)
    resp   = es.bulk(operations=operations)
    errors = [i for i in resp["items"] if "error" in i.get("index", {})]
    print(f"  {index}: {len(docs) - len(errors)}/{len(docs)} indexed" +
          (f" | {len(errors)} errors" if errors else ""))


