# ── Fingerprint ───────────────────────────────────────────────────────────────
def make_fingerprint(burn, velocity, sentiment, phase, team, tenure, milestone, margin):
    return [
        float(np.clip(burn,           -1, 1)),
        float(np.clip(velocity,       -1, 1)),
        float(np.clip(sentiment,      -1, 1)),
        float(PHASES.index(phase) / (len(PHASES) - 1)),
        float(min(team / 20,          1.0)),
        float(min(tenure / 36,        1.0)),
        float(min(milestone / 60,     1.0)),
        float(np.clip(margin / 50,    -1, 1)),
    ]


def jitter(value, noise=0.08):
    """Add small random noise to keep cluster members realistic but distinct."""
    return round(value + random.uniform(-noise, noise), 3)


def rand_date():
    start = date(2022, 1, 1)
    return (start + timedelta(days=random.randint(0, 900))).isoformat()


